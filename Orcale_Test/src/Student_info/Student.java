package Student_info;

public class Student {
	String 学号;
	String 姓名;
	String 性别;
	String 专业;
	int 年龄;
	public String get学号() {
		return 学号;
	}
	public void set学号(String 学号) {
		this.学号 = 学号;
	}
	public String get姓名() {
		return 姓名;
	}
	public void set姓名(String 姓名) {
		this.姓名 = 姓名;
	}
	public String get性别() {
		return 性别;
	}
	public void set性别(String 性别) {
		this.性别 = 性别;
	}
	public String get专业() {
		return 专业;
	}
	public void set专业(String 专业) {
		this.专业 = 专业;
	}
	public int get年龄() {
		return 年龄;
	}
	public void set年龄(int 年龄) {
		this.年龄 = 年龄;
	}
	

}
